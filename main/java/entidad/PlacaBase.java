package entidad;

public class PlacaBase {
	private int  id_placa_base, gene_ram_id, cate_id, precio_placa_base, stock_placa_base;
	private String  descri_placa_base;
	public int getId_placa_base() {
		return id_placa_base;
	}
	public void setId_placa_base(int id_placa_base) {
		this.id_placa_base = id_placa_base;
	}
	public int getGene_ram_id() {
		return gene_ram_id;
	}
	public void setGene_ram_id(int gene_ram_id) {
		this.gene_ram_id = gene_ram_id;
	}
	public int getCate_id() {
		return cate_id;
	}
	public void setCate_id(int cate_id) {
		this.cate_id = cate_id;
	}
	public int getPrecio_placa_base() {
		return precio_placa_base;
	}
	public void setPrecio_placa_base(int precio_placa_base) {
		this.precio_placa_base = precio_placa_base;
	}
	public int getStock_placa_base() {
		return stock_placa_base;
	}
	public void setStock_placa_base(int stock_placa_base) {
		this.stock_placa_base = stock_placa_base;
	}
	public String getDescri_placa_base() {
		return descri_placa_base;
	}
	public void setDescri_placa_base(String descri_placa_base) {
		this.descri_placa_base = descri_placa_base;
	}
}
