package entidad;

public class Ram {
	private Integer  id_ram, capaci_ram_id, genera_ram_id, cate_id, precio_ram;
	private String  descrip_ram;
	public Integer getId_ram() {
		return id_ram;
	}
	public void setId_ram(Integer id_ram) {
		this.id_ram = id_ram;
	}
	public Integer getCapaci_ram_id() {
		return capaci_ram_id;
	}
	public void setCapaci_ram_id(Integer capaci_ram_id) {
		this.capaci_ram_id = capaci_ram_id;
	}
	public Integer getGenera_ram_id() {
		return genera_ram_id;
	}
	public void setGenera_ram_id(Integer genera_ram_id) {
		this.genera_ram_id = genera_ram_id;
	}
	public Integer getCate_id() {
		return cate_id;
	}
	public void setCate_id(Integer cate_id) {
		this.cate_id = cate_id;
	}
	public Integer getPrecio_ram() {
		return precio_ram;
	}
	public void setPrecio_ram(Integer precio_ram) {
		this.precio_ram = precio_ram;
	}
	public String getDescrip_ram() {
		return descrip_ram;
	}
	public void setDescrip_ram(String descrip_ram) {
		this.descrip_ram = descrip_ram;
	}
}
